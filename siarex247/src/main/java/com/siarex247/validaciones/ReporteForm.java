package com.siarex247.validaciones;

public class ReporteForm {

	private long folioOrden;
	private int claveProveedor;
	private String etiquetaCartaPorte;
	private String valorXML;
	private String estatus;
	private String resultado;
	
	
	
	
	public long getFolioOrden() {
		return folioOrden;
	}
	public void setFolioOrden(long folioOrden) {
		this.folioOrden = folioOrden;
	}
	public int getClaveProveedor() {
		return claveProveedor;
	}
	public void setClaveProveedor(int claveProveedor) {
		this.claveProveedor = claveProveedor;
	}
	public String getEtiquetaCartaPorte() {
		return etiquetaCartaPorte;
	}
	public void setEtiquetaCartaPorte(String etiquetaCartaPorte) {
		this.etiquetaCartaPorte = etiquetaCartaPorte;
	}
	public String getValorXML() {
		return valorXML;
	}
	public void setValorXML(String valorXML) {
		this.valorXML = valorXML;
	}
	public String getEstatus() {
		return estatus;
	}
	public void setEstatus(String estatus) {
		this.estatus = estatus;
	}
	public String getResultado() {
		return resultado;
	}
	public void setResultado(String resultado) {
		this.resultado = resultado;
	}
	
	
	
	
}

package com.siarex247.configuraciones.RegimenFiscal;

public class RegimenForm {

	
	private String claveRegimen;
	private String descripcion;
	private String tipoPersona;
	private String fechaIni;
	private String fechaFin;
	
	
	public String getClaveRegimen() {
		return claveRegimen;
	}
	public void setClaveRegimen(String claveRegimen) {
		this.claveRegimen = claveRegimen;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public String getTipoPersona() {
		return tipoPersona;
	}
	public void setTipoPersona(String tipoPersona) {
		this.tipoPersona = tipoPersona;
	}
	public String getFechaIni() {
		return fechaIni;
	}
	public void setFechaIni(String fechaIni) {
		this.fechaIni = fechaIni;
	}
	public String getFechaFin() {
		return fechaFin;
	}
	public void setFechaFin(String fechaFin) {
		this.fechaFin = fechaFin;
	}
	
	
	
	
}

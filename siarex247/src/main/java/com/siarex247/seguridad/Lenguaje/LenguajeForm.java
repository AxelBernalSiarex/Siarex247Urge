package com.siarex247.seguridad.Lenguaje;

public class LenguajeForm {

	public String idPantalla;
	public String etiqueta;
	public String mensajeIngles;
	public String mensajeEspanol;
	
	
	public String getIdPantalla() {
		return idPantalla;
	}
	public void setIdPantalla(String idPantalla) {
		this.idPantalla = idPantalla;
	}
	public String getEtiqueta() {
		return etiqueta;
	}
	public void setEtiqueta(String etiqueta) {
		this.etiqueta = etiqueta;
	}
	public String getMensajeIngles() {
		return mensajeIngles;
	}
	public void setMensajeIngles(String mensajeIngles) {
		this.mensajeIngles = mensajeIngles;
	}
	public String getMensajeEspanol() {
		return mensajeEspanol;
	}
	public void setMensajeEspanol(String mensajeEspanol) {
		this.mensajeEspanol = mensajeEspanol;
	}
	
	
	
	
}

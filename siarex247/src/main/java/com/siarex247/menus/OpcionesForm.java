package com.siarex247.menus;

public class OpcionesForm {

	private int claveOpcion;
	private String nombreCorto;
	private String descripcion;
	private String urlOpcion;
	
	public int getClaveOpcion() {
		return claveOpcion;
	}
	public void setClaveOpcion(int claveOpcion) {
		this.claveOpcion = claveOpcion;
	}
	public String getNombreCorto() {
		return nombreCorto;
	}
	public void setNombreCorto(String nombreCorto) {
		this.nombreCorto = nombreCorto;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public String getUrlOpcion() {
		return urlOpcion;
	}
	public void setUrlOpcion(String urlOpcion) {
		this.urlOpcion = urlOpcion;
	}

	
	
	
}

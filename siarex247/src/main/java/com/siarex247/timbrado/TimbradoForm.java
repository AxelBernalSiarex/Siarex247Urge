package com.siarex247.timbrado;

public class TimbradoForm {

	
	private int idRegistro;
	private String empresa;
	private long folioEmpresa;
	private String tipoMetodo;
	private String uuid;
	private String code;
	private String respuesta;
	private String mensaje;
	
	public int getIdRegistro() {
		return idRegistro;
	}
	public void setIdRegistro(int idRegistro) {
		this.idRegistro = idRegistro;
	}
	public String getEmpresa() {
		return empresa;
	}
	public void setEmpresa(String empresa) {
		this.empresa = empresa;
	}
	public long getFolioEmpresa() {
		return folioEmpresa;
	}
	public void setFolioEmpresa(long folioEmpresa) {
		this.folioEmpresa = folioEmpresa;
	}
	public String getTipoMetodo() {
		return tipoMetodo;
	}
	public void setTipoMetodo(String tipoMetodo) {
		this.tipoMetodo = tipoMetodo;
	}
	public String getUuid() {
		return uuid;
	}
	public void setUuid(String uuid) {
		this.uuid = uuid;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getRespuesta() {
		return respuesta;
	}
	public void setRespuesta(String respuesta) {
		this.respuesta = respuesta;
	}
	public String getMensaje() {
		return mensaje;
	}
	public void setMensaje(String mensaje) {
		this.mensaje = mensaje;
	}
	
	
	
	
}

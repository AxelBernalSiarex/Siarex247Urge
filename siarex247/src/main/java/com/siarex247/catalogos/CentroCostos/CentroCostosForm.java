package com.siarex247.catalogos.CentroCostos;

public class CentroCostosForm {

	
	private int claveRegistro = 0;
	private String idCentroCosto;
	private String departamento;
	private String correoCentro;
	private String nombreCorto;
	
	
	
	public int getClaveRegistro() {
		return claveRegistro;
	}
	public void setClaveRegistro(int claveRegistro) {
		this.claveRegistro = claveRegistro;
	}
	
	public String getNombreCorto() {
		return nombreCorto;
	}
	public void setNombreCorto(String nombreCorto) {
		this.nombreCorto = nombreCorto;
	}
	public String getIdCentroCosto() {
		return idCentroCosto;
	}
	public void setIdCentroCosto(String idCentroCosto) {
		this.idCentroCosto = idCentroCosto;
	}
	public String getDepartamento() {
		return departamento;
	}
	public void setDepartamento(String departamento) {
		this.departamento = departamento;
	}
	public String getCorreoCentro() {
		return correoCentro;
	}
	public void setCorreoCentro(String correoCentro) {
		this.correoCentro = correoCentro;
	}
	
	
	
	
}

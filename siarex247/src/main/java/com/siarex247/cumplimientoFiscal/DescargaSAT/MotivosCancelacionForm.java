package com.siarex247.cumplimientoFiscal.DescargaSAT;

public class MotivosCancelacionForm {

	private String pacCertifico;
	private String estadoComprobante;
	private String fechaCancelacion;
	private String claveCancelacion;
	private String descripcionCancelacion;
	private String uuid;
	private String rfcEmisor;
	private String rfcReceptor;
	
	public String getPacCertifico() {
		return pacCertifico;
	}
	public void setPacCertifico(String pacCertifico) {
		this.pacCertifico = pacCertifico;
	}
	public String getEstadoComprobante() {
		return estadoComprobante;
	}
	public void setEstadoComprobante(String estadoComprobante) {
		this.estadoComprobante = estadoComprobante;
	}
	public String getFechaCancelacion() {
		return fechaCancelacion;
	}
	public void setFechaCancelacion(String fechaCancelacion) {
		this.fechaCancelacion = fechaCancelacion;
	}
	public String getClaveCancelacion() {
		return claveCancelacion;
	}
	public void setClaveCancelacion(String claveCancelacion) {
		this.claveCancelacion = claveCancelacion;
	}
	public String getDescripcionCancelacion() {
		return descripcionCancelacion;
	}
	public void setDescripcionCancelacion(String descripcionCancelacion) {
		this.descripcionCancelacion = descripcionCancelacion;
	}
	public String getUuid() {
		return uuid;
	}
	public void setUuid(String uuid) {
		this.uuid = uuid;
	}
	public String getRfcEmisor() {
		return rfcEmisor;
	}
	public void setRfcEmisor(String rfcEmisor) {
		this.rfcEmisor = rfcEmisor;
	}
	public String getRfcReceptor() {
		return rfcReceptor;
	}
	public void setRfcReceptor(String rfcReceptor) {
		this.rfcReceptor = rfcReceptor;
	}
	

	
}

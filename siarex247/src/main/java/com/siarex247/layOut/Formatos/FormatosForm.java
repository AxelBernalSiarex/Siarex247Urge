package com.siarex247.layOut.Formatos;

public class FormatosForm {

	private int idRegistro;
	private String descripcion;
	private String subjectCorreo;
	private String cuerpoCorreo;
	private String nombreArchivo;
	private String tipoProveedor;
	private String estatus;
	private String copiaPara;
	private String razonSocial;
	private String fechaTarea;
	private String usuarioHTTP;
	private String nombreUsuarioHTTP;
	
	
	public int getIdRegistro() {
		return idRegistro;
	}
	public void setIdRegistro(int idRegistro) {
		this.idRegistro = idRegistro;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public String getSubjectCorreo() {
		return subjectCorreo;
	}
	public void setSubjectCorreo(String subjectCorreo) {
		this.subjectCorreo = subjectCorreo;
	}
	public String getCuerpoCorreo() {
		return cuerpoCorreo;
	}
	public void setCuerpoCorreo(String cuerpoCorreo) {
		this.cuerpoCorreo = cuerpoCorreo;
	}
	public String getNombreArchivo() {
		return nombreArchivo;
	}
	public void setNombreArchivo(String nombreArchivo) {
		this.nombreArchivo = nombreArchivo;
	}
	public String getTipoProveedor() {
		return tipoProveedor;
	}
	public void setTipoProveedor(String tipoProveedor) {
		this.tipoProveedor = tipoProveedor;
	}
	public String getEstatus() {
		return estatus;
	}
	public void setEstatus(String estatus) {
		this.estatus = estatus;
	}
	public String getCopiaPara() {
		return copiaPara;
	}
	public void setCopiaPara(String copiaPara) {
		this.copiaPara = copiaPara;
	}
	public String getRazonSocial() {
		return razonSocial;
	}
	public void setRazonSocial(String razonSocial) {
		this.razonSocial = razonSocial;
	}
	public String getFechaTarea() {
		return fechaTarea;
	}
	public void setFechaTarea(String fechaTarea) {
		this.fechaTarea = fechaTarea;
	}
	public String getUsuarioHTTP() {
		return usuarioHTTP;
	}
	public void setUsuarioHTTP(String usuarioHTTP) {
		this.usuarioHTTP = usuarioHTTP;
	}
	public String getNombreUsuarioHTTP() {
		return nombreUsuarioHTTP;
	}
	public void setNombreUsuarioHTTP(String nombreUsuarioHTTP) {
		this.nombreUsuarioHTTP = nombreUsuarioHTTP;
	}
}